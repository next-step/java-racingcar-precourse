package racingcar.model;

public class RacingCarGameRule {
    private String racingCarGameNames;
    private int racingCarGameRound;

    public String getRacingCarGameNames() {
        return racingCarGameNames;
    }

    public void setRacingCarGameNames(String racingCarGameNames) {
        this.racingCarGameNames = racingCarGameNames;
    }

    public int getRacingCarGameRound() {
        return racingCarGameRound;
    }

    public void setRacingCarGameRound(int racingCarGameRound) {
        this.racingCarGameRound = racingCarGameRound;
    }
}
